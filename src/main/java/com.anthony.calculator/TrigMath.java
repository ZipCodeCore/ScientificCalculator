public class TrigMath {

    public double calcSine(double number1) {
        return Math.sin(number1);
    }

    public double calcCosine(double number1) {
        return Math.cos(number1);
    }

    public double calcTangent(double number1) {
        return Math.tan(number1);
    }

    public double calcInverseSine(double number1) {
        return Math.asin(number1);
    }

    public double calcInverseCosine(double number1) {
        return Math.acos(number1);
    }

    public double calcInverseTangent(double number1) {
        return Math.atan(number1);
    }
}
