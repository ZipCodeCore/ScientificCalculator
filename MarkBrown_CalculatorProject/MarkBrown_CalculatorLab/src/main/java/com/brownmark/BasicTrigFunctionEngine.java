public class BasicTrigFunctionEngine {



    public BasicTrigFunctionEngine() {}



    public double sineFunction(double operand) {return Math.sin(operand);}
    public double tanFunction(double operand) {return Math.tan(operand);}
    public double cosineFunction(double operand) {return Math.cos(operand);}


}
