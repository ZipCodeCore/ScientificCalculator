package com.brownmark;


public class Display {



    public Display () {}



    public void defaultDisplay() {System.out.println("0");}
    public void returnDisplay(double number) {System.out.println(number);}
    public void displayErr() {System.out.println("Err");}
    public void displayInvalidOperator() {System.out.println("Not a Valid Operator.");}
    public void displayInvalidInput() {System.out.println("Not a Valid Input");}



}
